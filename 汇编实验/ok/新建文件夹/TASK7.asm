	POP DS
	POP DI
	POP SI
	POP DX
	POP CX
	POP BX
	POP AX
	RET
	
PRINT	ENDP
	
FINDGOODS	PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH SI
	PUSH DS
TASK3:
GOODSINPUT: 
	LEA DX,INPUTGOODSNAME
	MOV AH,9
	INT 21H
	LEA DX,IN_GOODS
	MOV AH,10
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H	
	  
	MOV SI,OFFSET IN_GOODS
	MOV DI,OFFSET _GA1
	PUSH DI
	MOV BX,N
		
DEFCX:		
	MOV CX,[SI+1]
	MOV CH,0
	CMP CX,0
	JZ TASK3
	MOV DX,DI
						
CHECKGOODS: ;检验商品是否存在
	MOV AH,[SI+2]
	MOV AL,[DI]
	CMP AH,AL
	JNZ DIF
	INC SI
	INC DI
	LOOP CHECKGOODS
	JMP FINAL 
		
DIF:	
	POP DI
	ADD DI,21
	PUSH DI
	MOV SI,OFFSET IN_GOODS
	DEC BX
	CMP BX,0
	JZ  RESET
	JMP DEFCX
		
RESET:  
	LEA DX,NOTHAVEGOODS
	MOV AH,9
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H
	JMP GOODSINPUT
		
FINAL:	
	MOV AL,[DI]
	CMP AL,0
	JZ  O
	JMP DIF

O:
	POP DS
	POP DI
	POP SI
	POP DX
	POP CX
	POP BX
	POP AX
	RET
FINDGOODS	ENDP

