_WAN4	PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH SI
	PUSH DI
	PUSH DS

	MOV DI,OFFSET _GA1
	MOV CX,N
DO:
	PUSH DI
	ADD DI,21
	LOOP DO
	CALL SORT
	
	MOV SI,OFFSET SORTED
	MOV CX,N
P:
	POP WORD PTR[SI]
