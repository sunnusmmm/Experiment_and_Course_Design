PRINT	PROC ;传入找到商品的首地址DI
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH SI
	PUSH DI
	PUSH DS
	
	MOV CX,2
PRINT1:
	MOV CX,2
	MOV DL,[DI]
	CMP DL,0H
	JZ  ALLIMFOR
	MOV AH,2
	INT 21H
	INC DI
	LOOP PRINT1 

ALLIMFOR:
	POP DI
	PUSH DI
	MOV AH,2
	MOV DL,32
	INT 21H
	MOV AX,0
	MOV AL,[DI+10]
	CALL DTOC
	MOV CX,5
	ADD DI,11
